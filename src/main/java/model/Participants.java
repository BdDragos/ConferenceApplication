public class Participants
{
    private int idpa;
    private String typepa;
    private int ida;
    private int idat;
    private int ids;

    public int getIdpa() {
        return idpa;
    }

    public void setIdpa(int idpa) {
        this.idpa = idpa;
    }

    public String getTypepa() {
        return typepa;
    }

    public void setTypepa(String typepa) {
        this.typepa = typepa;
    }

    public int getIda() {
        return ida;
    }

    public void setIda(int ida) {
        this.ida = ida;
    }

    public int getIdat() {
        return idat;
    }

    public void setIdat(int idat) {
        this.idat = idat;
    }

    public int getIds() {
        return ids;
    }

    public void setIds(int ids) {
        this.ids = ids;
    }
}

